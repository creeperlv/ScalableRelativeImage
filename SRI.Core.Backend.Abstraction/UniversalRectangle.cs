namespace SRI.Core.Backend
{
    public struct UniversalRectangle
    {
        public float x;
        public float y;
        public float w;
        public float h;

        public UniversalRectangle(float x, float y, float w, float h)
        {
            this.x = x;
            this.y = y;
            this.w = w;
            this.h = h;
        }
    }
}
